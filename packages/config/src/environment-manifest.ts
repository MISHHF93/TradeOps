/**
 * TradeOps Environment Manifest — derived from repository scan (not speculative).
 *
 * Sources:
 * - process.env / env.* usage across apps, packages, scripts
 * - @tradeops/config domain modules (AI, security, capital, financial gates)
 * - Boot path: apps/api main (loadEnv + assertSecurityBoot + assertProductionEnv)
 *
 * Rules:
 * - Secrets never appear as values here or in docs.
 * - Tenant merchant credentials → encrypted connector vault (not global .env in multi-tenant prod).
 * - Compromised/chat-pasted keys must be rotated; never commit.
 */

export type EnvStorage = 'platform_env' | 'tenant_connector_vault' | 'browser_public' | 'os_only';

export type EnvVarManifest = {
  name: string;
  subsystem: string;
  secret: boolean;
  requiredInProduction: boolean;
  serverOnly: boolean;
  storage: EnvStorage;
  description: string;
  aliases?: string[];
  deprecated?: boolean;
  replacement?: string;
  safeDefault?: string;
};

function v(
  name: string,
  subsystem: string,
  opts: Omit<EnvVarManifest, 'name' | 'subsystem'>,
): EnvVarManifest {
  return { name, subsystem, ...opts };
}

