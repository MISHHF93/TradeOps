export function environmentManifestPublicStatus(
  env: NodeJS.ProcessEnv | Record<string, string | undefined> = process.env,
) {
  const present = (name: string) => Boolean((env[name] ?? '').toString().trim());
  const rows = PLATFORM_ENV_MANIFEST.map((row) => ({
    name: row.name,
    subsystem: row.subsystem,
    secret: row.secret,
    requiredInProduction: row.requiredInProduction,
    configured: present(row.name) || Boolean(row.aliases?.some((a) => present(a))),
    storage: row.storage,
    deprecated: Boolean(row.deprecated),
  }));
  const missingRequiredProd =
    env.NODE_ENV === 'production'
      ? rows.filter((r) => r.requiredInProduction && !r.configured).map((r) => r.name)
      : [];
  return {
    totalManifest: PLATFORM_ENV_MANIFEST.length,
    configured: rows.filter((r) => r.configured).length,
    missingRequiredProduction: missingRequiredProd,
    architecture: {
      aiPrimary: 'cohere',
      searchOwnedBy: 'tradeops_search_manager',
      tenantSecrets: 'encrypted_connector_vault',
      compromisedKeyPolicy: 'rotate_immediately_never_reuse_chat_pasted_keys',
    },
    ai: {
      provider: (env.AI_PROVIDER ?? 'cohere').toString(),
      cohereKeyConfigured: present('COHERE_API_KEY'),
      webSearchEnabled: present('WEB_SEARCH_ENABLED')
        ? ['1', 'true', 'yes', 'on'].includes(String(env.WEB_SEARCH_ENABLED).toLowerCase())
        : false,
      tavilyConfigured: present('TAVILY_API_KEY'),
      openaiConfigured: present('OPENAI_API_KEY'),
      xaiConfigured: present('XAI_API_KEY') || present('GROK_API_KEY'),
    },
    tenantScopedNote:
      'Merchant Shopify/Amazon/EasyPost/etc. tokens belong in encrypted connector vault, not global .env for multi-tenant production.',
    aliases: ENV_ALIASES,
    rows,
  };
}
